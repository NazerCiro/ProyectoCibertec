package clases;

public class clasepadre {
private int codigo, dni, telefono;
private String nombre, apellido, correo, direccion;
public clasepadre(int codigo, int dni, int telefono,String nombre, String apellido, String correo, String direccion) {
	this.codigo = codigo;
	this.dni = dni;
	this.telefono = telefono;
	this.nombre = nombre;
	this.apellido = apellido;
	this.correo = correo;
	this.direccion = direccion;
}


}
